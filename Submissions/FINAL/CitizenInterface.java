public interface CitizenInterface {

    /**
     * Sets the first name
     * @param inFirst the first name
     */
    void setFirstName(String inFirst);
    /**
     * Gets the first name.
     *
     * @return firstName the firstName
     */
    String getFirstName();
    /**
     * Sets the last name
     *
     * @param inLast the last name
     */
    void setLastName(String inLast);
    /**
     * Get the last name
     *
     * @return lastName the last name.
     */
    String getLastName();
}
